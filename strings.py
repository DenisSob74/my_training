example = "Топинамбур"
print(example[0])       # Вывод на экран первый символ строки
print(example[-1])      # Вывод на экран последний символ строки
print(example[5:10])    # Вывод на экран вторую половину этой строки
print(example[::-1])    # Вывод на экран это слово на оборот
print(example[1:10:2])  # Вывод на экран каждый второй символ этой строки
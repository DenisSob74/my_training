my_string = input("Ведите строку: ")

print("Вывод в верхнем регистре:", my_string .upper())
print("Вывод в нижнем регистре:", my_string .lower())
print("Вывод без пробелов", my_string.replace(" ",""))
print("Вывод на экран первый символ строки:", my_string[0])
print("Вывод на экран последнего симовола строки:", my_string[-1])